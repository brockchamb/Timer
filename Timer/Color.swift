        return UIColor(red:0.310,  green:0.478, blue:0.616, alpha:1.00)
    }
    static func babyBlue() -> UIColor {
        return UIColor(red:0.541, green:0.863, blue:0.980, alpha:1.00)
    }
    static func spaceGray() -> UIColor {
        return UIColor(red:0.796, green:0.839, blue:0.851, alpha:1.00)
    }
    static func blackNight() -> UIColor {
        return UIColor(red:0.157, green:0.169, blue:0.208, alpha:1.00)
    }
    static func grayNight() -> UIColor {
        return UIColor(red:0.431, green:0.435, blue:0.392, alpha:1.00)
    }
}
